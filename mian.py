# This is a basic Python program to demonstrate fundamental concepts

# Variables and basic data types
name = "Python Learner"    # String
age = 25                   # Integer
height = 5.9              # Float (decimal number)
is_learning = True        # Boolean

# Print statements and string formatting
print("Hello, I am learning Python!")
print(f"My name is {name}")  # Using f-string for formatting

# Basic arithmetic operations
x = 10
y = 5
print(f"\nBasic Math:")
print(f"Addition: {x + y}")
print(f"Subtraction: {x - y}")
print(f"Multiplication: {x * y}")
print(f"Division: {x / y}")

# Lists - a collection of items
fruits = ["apple", "banana", "orange"]
print(f"\nMy fruit list: {fruits}")
print(f"First fruit: {fruits[0]}")

# Simple if-else condition
if age >= 18:
    print("\nYou are an adult")
else:
    print("\nYou are a minor")

# Basic loop
print("\nCounting to 5:")
for i in range(1, 6):
    print(i)

print("hello world")